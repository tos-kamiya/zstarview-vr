import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const ROOT = process.cwd();
const inputPath = resolve(ROOT, 'data/stars.csv');
const outputBasePath = resolve(ROOT, 'src/generated/stars-data.js');
const outputExtra7Path = resolve(ROOT, 'src/generated/stars-data-extra-7.js');
const outputExtra8Path = resolve(ROOT, 'src/generated/stars-data-extra-8.js');

const BASE_MAX_VMAG = 6.0;
const EXTRA7_MAX_VMAG = 7.0;
const EXTRA8_MAX_VMAG = 8.0;

function bvToRgb(bvRaw) {
  const bv = Number.isFinite(bvRaw) ? bvRaw : NaN;
  if (Number.isNaN(bv)) return [248, 247, 255];
  if (bv < 0.0) return [170, 191, 255];
  if (bv < 0.3) return [202, 215, 255];
  if (bv < 0.6) return [248, 247, 255];
  if (bv < 1.0) return [255, 210, 161];
  return [255, 204, 111];
}

function brightnessFromVmag(vmag) {
  const bright = -1.5;
  const faint = BASE_MAX_VMAG;
  const t = Math.max(0, Math.min(1, (faint - vmag) / (faint - bright)));
  return 0.25 + 0.75 * Math.pow(t, 1.08);
}

function layerForVmag(vmag) {
  if (vmag <= 1.5) return 'bright';
  if (vmag <= 3.5) return 'mid';
  return 'faint';
}

function toCartesian(raHours, decDeg) {
  const ra = (raHours * Math.PI) / 12.0;
  const dec = (decDeg * Math.PI) / 180.0;
  const c = Math.cos(dec);
  const x = c * Math.cos(ra);
  const y = Math.sin(dec);
  // Keep RA increasing toward the east in our world coordinate system.
  const z = -c * Math.sin(ra);
  return [x, y, z];
}

function createEmptyLayers() {
  return {
    bright: { size: 2.8, opacity: 1.0, positions: [], colors: [] },
    mid: { size: 1.9, opacity: 0.95, positions: [], colors: [] },
    faint: { size: 1.1, opacity: 0.82, positions: [], colors: [] },
  };
}

const lines = readFileSync(inputPath, 'utf-8').split(/\r?\n/);
const baseLayers = createEmptyLayers();
const extra7Layers = createEmptyLayers();
const extra8Layers = createEmptyLayers();
let usedBase = 0;
let usedExtra7 = 0;
let usedExtra8 = 0;

for (let i = 1; i < lines.length; i += 1) {
  const line = lines[i];
  if (!line) continue;
  const cols = line.split(',');
  if (cols.length < 7) continue;

  const ra = Number.parseFloat(cols[3]);
  const dec = Number.parseFloat(cols[4]);
  const vmag = Number.parseFloat(cols[5]);
  const bv = Number.parseFloat(cols[6]);

  if (!Number.isFinite(ra) || !Number.isFinite(dec) || !Number.isFinite(vmag)) continue;
  if (vmag > EXTRA8_MAX_VMAG) continue;

  const [x, y, z] = toCartesian(ra, dec);
  const [r255, g255, b255] = bvToRgb(bv);
  const brightness = brightnessFromVmag(vmag);
  const color = [(r255 / 255) * brightness, (g255 / 255) * brightness, (b255 / 255) * brightness];
  const layerName = layerForVmag(vmag);

  if (vmag <= BASE_MAX_VMAG) {
    const layer = baseLayers[layerName];
    layer.positions.push(x, y, z);
    layer.colors.push(...color);
    usedBase += 1;
  } else if (vmag <= EXTRA7_MAX_VMAG) {
    const layer = extra7Layers[layerName];
    layer.positions.push(x, y, z);
    layer.colors.push(...color);
    usedExtra7 += 1;
  } else {
    const layer = extra8Layers[layerName];
    layer.positions.push(x, y, z);
    layer.colors.push(...color);
    usedExtra8 += 1;
  }
}

function layersToJsArray(layers) {
  return `[\n` +
    Object.entries(layers)
      .map(([name, layer]) => {
        return `  {\n` +
          `    name: '${name}',\n` +
          `    size: ${layer.size},\n` +
          `    opacity: ${layer.opacity},\n` +
          `    positions: [${layer.positions.join(',')}],\n` +
          `    colors: [${layer.colors.join(',')}],\n` +
          `  }`;
      })
      .join(',\n') +
    `\n]`;
}

const baseOutput = `// Auto-generated by scripts/generate-stars-data.mjs\n` +
`// Source: data/stars.csv\n` +
`// Filter: Vmag <= ${BASE_MAX_VMAG}\n` +
`export const STAR_META = ${JSON.stringify({ sourceRows: lines.length - 1, usedRows: usedBase, maxVmag: BASE_MAX_VMAG })};\n\n` +
`export const STAR_LAYERS = ${layersToJsArray(baseLayers)};\n`;

const extra7Output = `// Auto-generated by scripts/generate-stars-data.mjs\n` +
`// Source: data/stars.csv\n` +
`// Filter: ${BASE_MAX_VMAG} < Vmag <= ${EXTRA7_MAX_VMAG}\n` +
`export const STAR_EXTRA_7_META = ${JSON.stringify({ sourceRows: lines.length - 1, usedRows: usedExtra7, minExclusiveVmag: BASE_MAX_VMAG, maxVmag: EXTRA7_MAX_VMAG })};\n\n` +
`export const STAR_EXTRA_7_LAYERS = ${layersToJsArray(extra7Layers)};\n`;

const extra8Output = `// Auto-generated by scripts/generate-stars-data.mjs\n` +
`// Source: data/stars.csv\n` +
`// Filter: ${EXTRA7_MAX_VMAG} < Vmag <= ${EXTRA8_MAX_VMAG}\n` +
`export const STAR_EXTRA_8_META = ${JSON.stringify({ sourceRows: lines.length - 1, usedRows: usedExtra8, minExclusiveVmag: EXTRA7_MAX_VMAG, maxVmag: EXTRA8_MAX_VMAG })};\n\n` +
`export const STAR_EXTRA_8_LAYERS = ${layersToJsArray(extra8Layers)};\n`;

writeFileSync(outputBasePath, baseOutput, 'utf-8');
writeFileSync(outputExtra7Path, extra7Output, 'utf-8');
writeFileSync(outputExtra8Path, extra8Output, 'utf-8');
console.log(`Generated ${outputBasePath} with ${usedBase} stars.`);
console.log(`Generated ${outputExtra7Path} with ${usedExtra7} stars.`);
console.log(`Generated ${outputExtra8Path} with ${usedExtra8} stars.`);
