import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const ROOT = process.cwd();
const inputPath = resolve(ROOT, 'data/stars.csv');
const outputPath = resolve(ROOT, 'src/generated/famous-stars-data.js');
const ASTERISM_REQUIRED_SOURCE_IDS = new Set([
  'HIP32349', 'HIP37279', 'HIP27989', 'HIP26727', 'HIP26311', 'HIP25930', 'HIP24608', 'HIP21421',
  'HIP24436', 'HIP37826', 'HIP61084', 'HIP60718', 'HIP59747', 'HIP62434', 'HIP71683', 'HIP68702',
  'HIP45238', 'HIP52419', 'HIP48002', 'HIP50099', 'HIP42913', 'HIP45556', 'HIP45941', 'HIP41037',
  'HIP54061', 'HIP53910', 'HIP58001', 'HIP59774', 'HIP62956', 'HIP65378', 'HIP67301', 'HIP11767',
  'HIP85822', 'HIP82080', 'HIP77055', 'HIP72607', 'HIP75097', 'HIP79822', 'HIP69673', 'HIP65474',
  'HIP57632', 'HIP49669', 'HIP49583', 'HIP50583', 'HIP50335', 'HIP48455', 'HIP47908', 'HIP74946',
  'HIP82273', 'HIP77952', 'HIP91262', 'HIP102098', 'HIP97649', 'HIP100453', 'HIP95947', 'HIP97165',
  'HIP102488', 'HIP93864', 'HIP92855', 'HIP93506', 'HIP92104', 'HIP90496', 'HIP89931', 'HIP88635',
  'HIP90185', 'HIP84380', 'HIP81833', 'HIP81693', 'HIP83207', 'HIP113963', 'HIP113881', 'HIP677',
  'HIP1067', 'HIP114971', 'HIP115227', 'HIP115830', 'HIP116771', 'HIP117245', 'HIP116928', 'HIP115738',
  'HIP110960', 'HIP111497', 'HIP110395', 'HIP110672', 'HIP746', 'HIP3179', 'HIP4427', 'HIP6686',
  'HIP8886', 'HIP109492', 'HIP105199', 'HIP106032', 'HIP116727', 'HIP112724', 'HIP101769', 'HIP102281',
  'HIP102532', 'HIP101958', 'HIP101421',
]);

const lines = readFileSync(inputPath, 'utf-8').split(/\r?\n/);
const rows = [];
const asterismRows = [];

for (let i = 1; i < lines.length; i += 1) {
  const line = lines[i];
  if (!line) continue;
  const cols = line.split(',');
  if (cols.length < 7) continue;

  const sourceId = (cols[8] || '').trim();
  const raHours = Number.parseFloat(cols[3]);
  const decDeg = Number.parseFloat(cols[4]);
  const vmag = Number.parseFloat(cols[5]);
  if (!Number.isFinite(raHours) || !Number.isFinite(decDeg) || !Number.isFinite(vmag)) continue;

  if (ASTERISM_REQUIRED_SOURCE_IDS.has(sourceId)) {
    asterismRows.push({ name: (cols[2] || '').trim(), sourceId, raHours, decDeg, vmag });
  }

  const name = (cols[2] || '').trim();
  if (!name) continue;
  rows.push({ name, sourceId, raHours, decDeg, vmag });
}

rows.sort((a, b) => a.vmag - b.vmag);

const output =
  `// Auto-generated by scripts/generate-famous-stars-data.mjs\n` +
  `// Source: data/stars.csv (Name != empty)\n` +
  `export const FAMOUS_STAR_META = ${JSON.stringify({ sourceRows: lines.length - 1, usedRows: rows.length, asterismRows: asterismRows.length })};\n\n` +
  `export const FAMOUS_STARS = ${JSON.stringify(rows)};\n\n` +
  `export const ASTERISM_STARS = ${JSON.stringify(asterismRows)};\n`;

writeFileSync(outputPath, output, 'utf-8');
console.log(`Generated ${outputPath} with ${rows.length} stars.`);
